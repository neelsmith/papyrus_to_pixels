function mermaid(s)
	@htl """
	<html>
		<body>
			<script src="https://cdn.jsdelivr.net/npm/mermaid/dist/mermaid.min.js"/>
			<script>
				mermaid.initialize({ startOnLoad: true });
			</script>
			<pre class="mermaid">
			$s
			</pre>
		</body>
	</html>"""
end


