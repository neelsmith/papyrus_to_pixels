# ╔═╡ 7ff4539d-bb58-47b3-93cf-e0922755f4df
function dnaparser(src)
	labelleddata = []
	species = ""
	data = ""
	for ln in src
		if startswith(ln, ">")
			if ! isempty(species)
				push!(labelleddata, (species = species, dna = data))
			end
			species = replace(ln, ">" => "")
			data = ""
		else
			data = ln
		end
	end
	push!(labelleddata,(species = species, dna = data))
	labelleddata
end

