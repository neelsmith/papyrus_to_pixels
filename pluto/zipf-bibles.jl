# ╔═╡ db455f9d-83c8-4217-b114-fe950244eeb6
isnothing(f) ? "Nothing yet." : f

# ╔═╡ b68483f8-0f72-4907-922b-33dce51d888f
lines = isnothing(f) ? [] : split(String(f["data"]), "\n")

# ╔═╡ fa5305b8-6573-4319-8aa8-b83bcc3e2d4c
fulltext = begin
	re = r"([^ ]+) ([^ ]+) (.+)"
	passages = []
	for ln in lines
		m = match(re, ln)
		if ! isnothing(m)
			(bk, ref, psg) = m.captures
			push!(passages, psg)
		end
	end
	join(passages," ")
end

# ╔═╡ 2619274c-1397-499d-a1ca-888183002396
"""Tidy up text by removing punctuation and making all lowercase."""
function tidyup(txt)
		alphas = filter(c -> ! ispunct(c), txt)
		map(c -> lowercase(c), alphas)
end

# ╔═╡ ee83b946-8351-4fc0-9d03-058a0769d429
tidytext = tidyup(fulltext)


# ╔═╡ edc8f1d4-dfea-4e32-a2cc-f902816a1a4f
chunked = slidingwindow(split(tidytext), n = n)

# ╔═╡ 9488956a-8361-4347-b8de-9418cf5fff5a
stringchunks = map(chunked) do v
	join(v,"_")
end

# ╔═╡ c118e227-0efa-4bb5-af15-a7aa1703d83d
freqs_sorted = begin
	freqs = countmap(stringchunks) |> OrderedDict
	sort(freqs, byvalue=true, rev=true)
end

